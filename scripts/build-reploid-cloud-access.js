#!/usr/bin/env node
/**
 * Build sealed browser-cloud access windows from local operator secrets.
 *
 * Inputs:
 * - `.env` with `GEMINI_API_KEY`
 * - optional `REPLOID_ROOT_ACCESS_SECRET`
 * - optional `REPLOID_ACCESS_WINDOW_DAYS`
 * - optional `REPLOID_ACCESS_START_DATE` (YYYY-MM-DD)
 * - optional `REPLOID_ACCESS_CODEBOOK_PATH`
 *
 * Outputs:
 * - `src/self/cloud-access-windows.js` with sealed blobs only
 * - `.reploid-cloud/codebook-<year>.json` with operator-only access codes
 */

import fs from 'node:fs';
import path from 'node:path';
import dotenv from 'dotenv';
import { fileURLToPath } from 'node:url';
import { createHmac, randomBytes, webcrypto } from 'node:crypto';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const PROJECT_ROOT = path.join(__dirname, '..');

const DEFAULT_PROVIDER = 'gemini';
const DEFAULT_MODEL = 'gemini-3.1-flash-lite-preview';
const DEFAULT_WINDOW_DAYS = 365;
const DEFAULT_PAYLOAD_VERSION = 1;

const ensureDir = (dirPath) => {
  fs.mkdirSync(dirPath, { recursive: true });
};

const resolveProjectPath = (projectRoot, targetPath) => {
  if (!targetPath) return null;
  return path.isAbsolute(targetPath)
    ? targetPath
    : path.resolve(projectRoot, targetPath);
};

const formatDateLabel = (date) => {
  const year = date.getUTCFullYear();
  const month = String(date.getUTCMonth() + 1).padStart(2, '0');
  const day = String(date.getUTCDate()).padStart(2, '0');
  return `${year}-${month}-${day}`;
};

const parseStartDate = (value) => {
  if (!value) {
    const today = new Date();
    return new Date(Date.UTC(
      today.getUTCFullYear(),
      today.getUTCMonth(),
      today.getUTCDate()
    ));
  }
  const parsed = new Date(`${String(value).trim()}T00:00:00.000Z`);
  if (Number.isNaN(parsed.getTime())) {
    throw new Error(`Invalid REPLOID_ACCESS_START_DATE: ${value}`);
  }
  return parsed;
};

const addUtcDays = (date, offset) => {
  const next = new Date(date.getTime());
  next.setUTCDate(next.getUTCDate() + offset);
  return next;
};

const deriveCodeFromSecret = (rootAccessSecret, label) => {
  const digest = createHmac('sha256', rootAccessSecret)
    .update(`reploid-access:${label}`)
    .digest('base64url')
    .toUpperCase()
    .replace(/[^A-Z0-9]/g, '');

  const compact = digest.slice(0, 20);
  return `${compact.slice(0, 5)}-${compact.slice(5, 10)}-${compact.slice(10, 15)}-${compact.slice(15, 20)}`;
};

const createRandomCode = () => {
  const raw = randomBytes(16).toString('base64url').toUpperCase().replace(/[^A-Z0-9]/g, '').slice(0, 20);
  return `${raw.slice(0, 5)}-${raw.slice(5, 10)}-${raw.slice(10, 15)}-${raw.slice(15, 20)}`;
};

const stableStringify = (value) => JSON.stringify(value, null, 2);

const toCodebookPath = (projectRoot, startDate, env = process.env) => {
  const explicit = String(env.REPLOID_ACCESS_CODEBOOK_PATH || '').trim();
  if (explicit) return resolveProjectPath(projectRoot, explicit);
  return path.join(projectRoot, '.reploid-cloud', `codebook-${startDate.getUTCFullYear()}.json`);
};

const readCodebook = (codebookPath) => {
  if (!fs.existsSync(codebookPath)) return null;
  return JSON.parse(fs.readFileSync(codebookPath, 'utf8'));
};

const writeCodebook = (codebookPath, codebook) => {
  ensureDir(path.dirname(codebookPath));
  fs.writeFileSync(codebookPath, `${stableStringify(codebook)}\n`, 'utf8');
  fs.chmodSync(codebookPath, 0o600);
};

export const buildCodeEntries = ({ startDate, days, rootAccessSecret, existingEntries = [] }) => {
  const existingMap = new Map(
    existingEntries
      .filter((entry) => entry?.label && entry?.accessCode)
      .map((entry) => [entry.label, entry.accessCode])
  );

  const entries = [];
  for (let index = 0; index < days; index += 1) {
    const label = formatDateLabel(addUtcDays(startDate, index));
    const accessCode = existingMap.get(label)
      || (rootAccessSecret ? deriveCodeFromSecret(rootAccessSecret, label) : createRandomCode());
    entries.push({ label, accessCode });
  }
  return entries;
};

const writeGeneratedModule = (outputPath, payload, header) => {
  ensureDir(path.dirname(outputPath));
  const source = `${header}
 *
 * Generated by scripts/build-reploid-cloud-access.js. Do not edit manually.
 */

export const ${payload.exportName} = Object.freeze(${stableStringify(payload.value)});

export default ${payload.exportName};
`;

  fs.writeFileSync(outputPath, source, 'utf8');
};

export async function buildCloudAccessArtifacts(options = {}) {
  const projectRoot = options.projectRoot || PROJECT_ROOT;
  const env = options.env || process.env;
  const outputPath = resolveProjectPath(projectRoot, options.outputPath)
    || path.join(projectRoot, 'src', 'self', 'cloud-access-windows.js');
  const statusOutputPath = resolveProjectPath(projectRoot, options.statusOutputPath)
    || path.join(projectRoot, 'src', 'self', 'cloud-access-status.js');
  const provider = String(env.REPLOID_CLOUD_PROVIDER || DEFAULT_PROVIDER).trim() || DEFAULT_PROVIDER;
  const model = String(env.REPLOID_CLOUD_MODEL || DEFAULT_MODEL).trim() || DEFAULT_MODEL;
  const days = Math.max(
    1,
    Number.parseInt(env.REPLOID_ACCESS_WINDOW_DAYS || `${DEFAULT_WINDOW_DAYS}`, 10) || DEFAULT_WINDOW_DAYS
  );
  const startDate = parseStartDate(env.REPLOID_ACCESS_START_DATE);
  const codebookPath = resolveProjectPath(projectRoot, options.codebookPath)
    || toCodebookPath(projectRoot, startDate, env);
  const geminiApiKey = String(env.GEMINI_API_KEY || '').trim();
  const rootAccessSecret = String(env.REPLOID_ROOT_ACCESS_SECRET || '').trim() || null;

  if (!geminiApiKey) {
    return {
      outputPath,
      statusOutputPath,
      codebookPath,
      generatedPayload: {
        version: DEFAULT_PAYLOAD_VERSION,
        provider,
        model,
        generatedAt: new Date().toISOString(),
        windows: []
      },
      statusPayload: {
        version: DEFAULT_PAYLOAD_VERSION,
        provider,
        model,
        generatedAt: new Date().toISOString(),
        availableLabels: []
      },
      codebook: null
    };
  }

  const codebook = readCodebook(codebookPath);
  const { sealString } = await import('../src/self/key-unsealer.js');
  const entries = buildCodeEntries({
    startDate,
    days,
    rootAccessSecret,
    existingEntries: codebook?.entries || []
  });

  const sealedWindows = [];
  for (const entry of entries) {
    const blob = await sealString({
      accessCode: entry.accessCode,
      plaintext: JSON.stringify({
        apiKey: geminiApiKey,
        provider,
        model
      }),
      label: entry.label,
      cryptoApi: webcrypto
    });

    sealedWindows.push({
      label: entry.label,
      provider,
      model,
      blob
    });
  }

  return {
    outputPath,
    statusOutputPath,
    codebookPath,
    generatedPayload: {
      version: DEFAULT_PAYLOAD_VERSION,
      provider,
      model,
      generatedAt: new Date().toISOString(),
      windows: sealedWindows
    },
    statusPayload: {
      version: DEFAULT_PAYLOAD_VERSION,
      provider,
      model,
      generatedAt: new Date().toISOString(),
      availableLabels: entries.map((entry) => entry.label)
    },
    codebook: {
      version: DEFAULT_PAYLOAD_VERSION,
      provider,
      model,
      startDate: formatDateLabel(startDate),
      generatedAt: new Date().toISOString(),
      entries
    }
  };
}

export async function provisionReploidCloudAccess(options = {}) {
  const artifacts = await buildCloudAccessArtifacts(options);
  writeGeneratedModule(
    artifacts.outputPath,
    {
      exportName: 'GENERATED_REPLOID_CLOUD_ACCESS',
      value: artifacts.generatedPayload
    },
    `/**
 * @fileoverview Generated sealed access windows for browser-cloud inference.`
  );
  writeGeneratedModule(
    artifacts.statusOutputPath,
    {
      exportName: 'GENERATED_REPLOID_CLOUD_ACCESS_STATUS',
      value: artifacts.statusPayload
    },
    `/**
 * @fileoverview Generated provisioned access-window summary for boot-time inference status.`
  );
  if (artifacts.codebook) {
    writeCodebook(artifacts.codebookPath, artifacts.codebook);
  }
  return {
    outputPath: artifacts.outputPath,
    statusOutputPath: artifacts.statusOutputPath,
    codebookPath: artifacts.codebookPath,
    windowCount: artifacts.generatedPayload.windows.length,
    generatedPayload: artifacts.generatedPayload
  };
}

export async function main() {
  dotenv.config();
  const result = await provisionReploidCloudAccess();
  if (result.windowCount === 0) {
    console.warn('[cloud-access] GEMINI_API_KEY missing. Wrote empty access windows.');
    console.log(`[cloud-access] Output module: ${path.relative(PROJECT_ROOT, result.outputPath)}`);
    console.log(`[cloud-access] Status module: ${path.relative(PROJECT_ROOT, result.statusOutputPath)}`);
    return;
  }
  console.log(`[cloud-access] Wrote sealed windows to ${path.relative(PROJECT_ROOT, result.outputPath)}`);
  console.log(`[cloud-access] Wrote access summary to ${path.relative(PROJECT_ROOT, result.statusOutputPath)}`);
  console.log(`[cloud-access] Operator codebook: ${path.relative(PROJECT_ROOT, result.codebookPath)}`);
  console.log(`[cloud-access] Provisioned ${result.windowCount} daily access windows`);
}

const isMain = process.argv[1] && path.resolve(process.argv[1]) === __filename;

if (isMain) {
  main().catch((error) => {
    console.error('[cloud-access] Failed to build access windows');
    console.error(error);
    process.exit(1);
  });
};
